public class while_loop 
{

 public static void main(String[] args) 
 {
	 //* * * * 
	 //* * * * 
	 //* * * * 
	 
	
	//outer loop-->rows
	 for(int i=1;i<=15;i++)
	 {
		 //inner for loop-->column
		for(int j=1;j<=10;j++) 
		{
			System.out.print("* ");
		}
		System.err.println();
	 }
	
	
 }

 }