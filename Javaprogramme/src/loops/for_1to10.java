package loops;

public class for_1to10 
{
   public static void main(String[] args) 
   {
	   
	   
		   for(int i=1; i<=10;i++)
		   {
			   System.out.println(i);
		   }
		   
		   //print hi 5 times
		   
		  for(int i=1; i<=5;i++)
		  {
			  System.out.println("hi");
		  }
		  
		  //even number
		  for(int i=2; i<=10;i=i+2)
		  {
			  System.out.println(i);
		  }
		  //odd number
	   
		  for(int i=1;i<=10;i=i+2)
		  {
			  System.out.println(i);
		  }
		  
		  //reverse 10 to 1
		  
		 for(int i=10; i>=1;i--)
		 {
			 System.out.println(i);
		 }
		 
		 //reverse even number
		 for(int i=10; i>=2;i=i-2)
		 {
			 System.out.println(i);
		 }
	
	   
}
}
